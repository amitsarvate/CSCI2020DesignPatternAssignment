/**
 * subclass of Animal for Dog
 */
public class Dog extends Animal {

    /**
     *  getter
     * @return species of the animal
     */
    public String getSpecies() {
        return "Canis Familiaris";
    }

    /**
     * getter
     * @return sound of the animal
     */
    public String getSound() {
        return "Woof";
    }

}

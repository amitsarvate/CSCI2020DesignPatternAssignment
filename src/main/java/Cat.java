/**
 * subclass of Animal for Cat
 */
public class Cat extends Animal {

    /**
     *  getter
     * @return species of the animal
     */
    public String getSpecies() {
        return "Felis Catus";
    }


    /**
     * getter
     * @return sound of the animal
     */
    public String getSound() {
        return "Meow";
    }

}
